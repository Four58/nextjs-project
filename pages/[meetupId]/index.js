export const getStaticProps = async (context) => {
  //fetch data
  const meetupId = context.params.meetupId; //meetupId should be the same name as a file bracket name
  console.log(meetupId);

  const client = await MongoClient.connect(
    "mongodb+srv://snowman:fcirUH1YH9PWBcKj@cluster0.x4m0j.mongodb.net/meetups?retryWrites=true&w=majority"
  );

  const db = client.db();

  const meetupsCollection = db.collection("meetups");

  const selectedMeetup = await meetupsCollection.findOne({
    _id: ObjectId(meetupId), //change to object first to look for id in mongodb
  });

  return {
    props: {
      meetupData: {
        id: selectedMeetup._id.toString(), //convert object to string
        title: selectedMeetup.title,
        address: selectedMeetup.address,
        image: selectedMeetup.image,
        description: selectedMeetup.description,
      },
    },
  };
};

